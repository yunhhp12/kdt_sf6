# 연산자 (operator)
# 산술 연산자 -, +, *, /, // (몫), % (나머지)
# 입력
n1 = 10
n2 = 20

#출력
print(n1, n2)
print(n1 + n2)
print(n1 - n2)
print(n1 * n2)
print(n1 / n2)
print(n1 // n2)
print(n1 % n2)


#실습
num_bread = 30
num_ppl = 4

bread_each = num_bread // num_ppl
bread_remain = num_bread % num_ppl

print(f"빵의 개수: {bread_each}")
print(f"남은 빵의 개수: {bread_remain}")
